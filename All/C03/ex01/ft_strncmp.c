int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				d;
	unsigned int	i;

	i = 0;
	d = 0;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' && i < n)
	{
		if (s2[i] == '\0')
		{
			d = s1[i] - 0;
			break ;
		}
		if (s1[i] != s2[i])
		{
			d = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	if (s1[i] == '\0' && s2[i] != '\0' && i < n)
		d = 0 - s2[i];
	return (d);
}
